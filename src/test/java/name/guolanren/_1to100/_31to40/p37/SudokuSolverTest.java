package name.guolanren._1to100._31to40.p37;

import org.junit.Before;
import org.junit.Test;

import static org.junit.Assert.*;

/**
 * @author guolanren
 */
public class SudokuSolverTest {

    private SudokuSolver sudokuSolver;

    @Before
    public void before() {
        sudokuSolver = new SudokuSolver();
    }

    @Test
    public void test() {
        char[][] board = {{'5','3','.','.','7','.','.','.','.'},
                            {'6','.','.','1','9','5','.','.','.'},
                            {'.','9','8','.','.','.','.','6','.'},
                            {'8','.','.','.','6','.','.','.','3'},
                            {'4','.','.','8','.','3','.','.','1'},
                            {'7','.','.','.','2','.','.','.','6'},
                            {'.','6','.','.','.','.','2','8','.'},
                            {'.','.','.','4','1','9','.','.','5'},
                            {'.','.','.','.','8','.','.','7','9'}};
        
        char[][] expectBoard = {{'5','3','4','6','7','8','9','1','2'},
                            {'6','7','2','1','9','5','3','4','8'},
                            {'1','9','8','3','4','2','5','6','7'},
                            {'8','5','9','7','6','1','4','2','3'},
                            {'4','2','6','8','5','3','7','9','1'},
                            {'7','1','3','9','2','4','8','5','6'},
                            {'9','6','1','5','3','7','2','8','4'},
                            {'2','8','7','4','1','9','6','3','5'},
                            {'3','4','5','2','8','6','1','7','9'}};

        sudokuSolver.solveSudoku(board);
        assertArrayEquals(expectBoard, board);
    }
}